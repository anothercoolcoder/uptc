package repaso_parcial_2.model;

public class peliculaModel {
    int id;
    String titulo;
    String director;
    int year;
    public peliculaModel() {
    }
    public peliculaModel(int id, String titulo, String director, int year) {
        this.id = id;
        this.titulo = titulo;
        this.director = director;
        this.year = year;
    }
    
}
